package org.example.putPojo;

public class putInputObj {

    private String name;
    private String job;

    public putInputObj() {

    }

    public putInputObj(String name, String job) {
        this.name = name;
        this.job = job;
    }

    public String getName() {
        return name;
    }

    public String getJob() {
        return job;
    }
}
