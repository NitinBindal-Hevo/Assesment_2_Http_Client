package org.example.putPojo;

public class PutInputObj {

    private String name;
    private String job;

    public PutInputObj() {

    }

    public PutInputObj(String name, String job) {
        this.name = name;
        this.job = job;
    }

    public String getName() {
        return name;
    }

    public String getJob() {
        return job;
    }
}
