package org.example.postPojo;

public class postInputObj {

    String name;
    String job;

    public postInputObj() {

    }

    public postInputObj(String name, String job) {
        this.name = name;
        this.job = job;
    }

    public String getName() {
        return name;
    }

    public String getJob() {
        return job;
    }
}
